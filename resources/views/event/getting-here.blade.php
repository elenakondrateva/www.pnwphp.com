    <h3>Light Rail</h3>
    <p>The easiest way to access the University of Washington campus from Downtown Seattle or SeaTac airport is via <a href="https://www.soundtransit.org/schedules/link-light-rail">Sound Transit's Link Light Rail</a>. University of Washington Station is a half mile walk from Kane and Johnson halls. If you're looking to shorten that walk, there are <a href="http://pugetsound.onebusaway.org/where/standard/stop.action?id=1_29405">14 routes</a> that stop across the street from the station that will get you much closer to campus and the conference hotels. Check out <a href="https://www.seattletransitblog.com/seattle-for-visitors/">Seattle Transit Blog's <em>Seattle For Visitors</em></a> and grab the <a href="https://transitapp.com/">Transit</a> app for on-the-go information.</p>

    <h3>Driving</h3>
    <p>Those looking to drive will find parking in the UW's Central Plaza Garage. Parking is $15 per day ($10 on Saturday) and has direct elevator access to Kane hall. The entrance is on the east side of the intersection of <a href="https://www.google.com/maps?q=15th+Ave+NE+%26+NE+41st+St,+Seattle,+WA+98105">15th Ave NE &amp; NE 41st St</a>. <a href="http://en.parkopedia.com/parking/locations/kane_hall_(kne)_spokane_lane_seattle_wa_united_states_c23p0g95ypp/?arriving=201709080830&leaving=201709081830">Slightly cheaper alternatives</a> exist in the surrounding neighborhood. More information on campus visitor parking can be found <a href="http://www.washington.edu/facilities/transportation/parking-daily-visitor">here</a>.</p>

    <h3>Express Bus</h3>
    <p>There are several express bus routes that operate to the UW campus; schedules can be found with the operator:
        <ul>
            <li><a href="https://www.communitytransit.org/busservice/schedules#seattle-(u-district)">Community Transit</a> (Snohomish county)</li>
            <li><a href="https://www.soundtransit.org/Schedules/ST-Express-Bus">Sound Transit</a> (King and Pierce counties)</li>
            <li><a href="http://kingcounty.gov/depts/transportation/metro/schedules-maps.aspx">King County Metro</a></li>
        </ul>
    The <a href="http://www.soundtransit.org/Trip-Planner"><strong>Regional Trip Planner</strong></a> can help with routes and schedules.</p>

    <h3>Drop off</h3>
    <p>If you are being dropped off on campus, there is a drop off point immediately north of Kane Hall at <a href="https://www.google.com/maps/place/War+Memorial+Flagpole/@47.6570339,-122.3097427">the War Memorial Flagpole</a> which is at the south end of Memorial Way and is accessed from NE 45th St &amp; 17th Ave NE.</p>

    <h3>Bicycle</h3>
    <p>The University of Washington campus has ample bicycle parking near each building.</p>

    <h3>Accessibility</h3>
    <p>For accessibility information, see UW Facility Services' pages for <a href="http://assetmapper.fs.washington.edu/ada/uw.ada/building.aspx?id=73">Kane Hall</a> and <a href="http://assetmapper.fs.washington.edu/ada/uw.ada/building.aspx?id=72">Johnson Hall</a></p>
